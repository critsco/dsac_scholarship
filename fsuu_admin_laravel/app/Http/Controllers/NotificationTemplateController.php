    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $data = Notification::query();

        if ($request->has("system_id")) {
            $data->where("system_id", $request->system_id);
        }

        if ($request->sort_field && $request->sort_order) {
            if (
                $request->sort_field != '' && $request->sort_field != 'undefined' && $request->sort_field != 'null'  &&
                $request->sort_order != ''  && $request->sort_order != 'undefined' && $request->sort_order != 'null'
            ) {
                $data = $data->orderBy(isset($request->sort_field) ? $request->sort_field : 'id', isset($request->sort_order)  ? $request->sort_order : 'desc');
            }
        } else {
            $data = $data->orderBy('id', 'desc');
        }

        if ($request->page_size) {
            $data = $data->limit($request->page_size)
                ->paginate($request->page_size, ['*'], 'page', $request->page)
                ->toArray();
        } else {
            $data = $data->get();
        }

        return response()->json([
            'success'   => true,
            'data'      => $data,
        ], 200);
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $ret = [
            "success" => false,
            "message" => "Notification template not " . ($request->id ? "updated" : "created") . "."
        ];

        $data = $request->validate([
            "title" => "required",
            "subject" => "required",
            "body" => "required",
            "system_id" => "required"
        ]);

        $notification_template = Notification::updateOrCreate(
            ["id" => $request->id],
            $data
        );

        if ($notification_template) {
            $ret = [
                "success" => true,
                "message" => "Notification template " . ($request->id ? "updated" : "created") . " successfully.",
            ];
        }

        return response()->json($ret, 200);
    }

